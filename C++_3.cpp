#include<iostream>
using namespace std;
int main()
{
	int a,b;
	cout<<"Enter two numbers:";
	cin>>a>>b;
	cout<<"The sum of two numbers:"<<a+b<<endl;
	cout<<"The difference is:"<<a-b<<endl;
	cout<<"The product is:"<<a * b<<endl;
	cout<<"The division is:"<<a / b<<endl;
	cout<<"The modulus is:"<<a % b<<endl;
	cout<<"The AND value is:"<<(a & b)<<endl;
	cout<<"The OR value is:"<<(a | b)<<endl;
	cout<<"The XOR value is:"<<(a ^ b)<<endl;
	cout<<"The post increament value is:"<<a++<<endl;
	cout<<"the post decreament value is:"<<a--<<endl;
	cout<<"The pre increament value is:"<<++a<<endl;
	cout<<"The pre decreament value is:"<<--a<<endl;
}
